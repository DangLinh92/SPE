CREATE PROC [dbo].[PKG_BUSINESS_SP@GET_SPARE_PART_PAY_BY_CODE](
@A_DEPARTMENT		NVARCHAR(50),
@A_SPARE_PART_CODE NVARCHAR(50),
@N_RETURN			int				OUTPUT,
@V_RETURN			NVARCHAR(4000)	OUTPUT
)
AS

BEGIN TRY
		BEGIN
		    SET NOCOUNT OFF;  
			SELECT [ID],[SPARE_PART_CODE],[DATE_LEND],[QUANTITY],[QUANTITY_PAY],[UNIT],[DATE_PAY],[DATE_PAY_ACTUAL],[IS_PAY],[LEND_CODE]
			FROM [dbo].[EWIP_SPAREPART_LEND]
			WHERE DEPT_CODE = @A_DEPARTMENT AND [SPARE_PART_CODE] = @A_SPARE_PART_CODE
			ORDER BY [LEND_CODE]
		END

	SET @N_RETURN = 0;
	SET @V_RETURN = 'MSG_COM_004';
END TRY
	BEGIN CATCH
  SET @N_RETURN = ERROR_NUMBER();
  SET @V_RETURN = ERROR_MESSAGE();
END CATCH


GO